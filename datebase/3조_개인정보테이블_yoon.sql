-----테이블 생성-----

-- 팀장 평가
CREATE TABLE LEADER_EVAL(
    NO                  NUMBER  PRIMARY KEY
    , EVALUATOR_NO      NUMBER
    , EVALUATEE_NO      NUMBER
    , COMMUNICATION     NUMBER  NOT NULL
    , RESPONSIBILITY    NUMBER  NOT NULL
    , JUDGEMENT         NUMBER  NOT NULL
    , PROFESSIONALISM   NUMBER  NOT NULL
    , START_DATE        DATE
    , END_DATE          DATE
    , WRITE_DATE        DATE    DEFAULT SYSDATE
    , MODIFY_DATE       DATE
    , COMPLIMENT        VARCHAR2(1000)
    , DISAPPOINTMENT    VARCHAR2(1000)
    , SUGGESTION        VARCHAR2(1000)
);

-- 팀원 평가
CREATE TABLE MEMBER_EVAL(
    NO                  NUMBER  PRIMARY KEY
    , EVALUATOR_NO      NUMBER
    , EVALUATEE_NO      NUMBER
    , POTENTIAL         NUMBER  NOT NULL
    , COMMUNICATION     NUMBER  NOT NULL
    , PROBLEM_SOLVE     NUMBER  NOT NULL
    , RESPONSIBILITY    NUMBER  NOT NULL
    , START_DATE        DATE    
    , END_DATE          DATE
    , WRITE_DATE        DATE    DEFAULT SYSDATE
    , MODIFY_DATE       DATE
    , COMPLIMENT        VARCHAR2(1000)
    , DISAPPOINTMENT    VARCHAR2(1000)
    , SUGGESTION        VARCHAR2(1000)
);
-- 범위설정
-- 동료 평가
CREATE TABLE COLLEAGE_EVAL(
    NO                  NUMBER  PRIMARY KEY
    , EVALUATOR_NO      NUMBER
    , EVALUATEE_NO      NUMBER
    , POTENTIAL         NUMBER  NOT NULL
    , COMMUNICATION     NUMBER  NOT NULL
    , PROBLEM_SOLVE     NUMBER  NOT NULL
    , RESPONSIBILITY    NUMBER  NOT NULL
    , START_DATE        DATE
    , END_DATE          DATE
    , WRITE_DATE        DATE    DEFAULT SYSDATE
    , MODIFY_DATE       DATE
    , COMPLIMENT        VARCHAR2(1000)
    , DISAPPOINTMENT    VARCHAR2(1000)
    , SUGGESTION        VARCHAR2(1000)
);

-- 성과지표
CREATE TABLE KPI(
    NO                  NUMBER  PRIMARY KEY
    , EMP_NO            NUMBER
    , PROJECT_NO        NUMBER
    , GOAL              VARCHAR2(1000)
    , PERSONAL_SCHECULE VARCHAR2(1000)
    , ENROLL_DATE       DATE    DEFAULT SYSDATE
    , MODIFY_DATE       DATE   
    , FOCUS             NUMBER
);

-- 출퇴근
CREATE TABLE COMMUTE(
    NO          NUMBER  PRIMARY KEY
    , EMP_NO    NUMBER
    , WORK_DATE DATE    DEFAULT SYSDATE
    , WORK_TIME DATE
    , QUIT_TIME DATE
);

-- 건의게시판
CREATE TABLE TENDINOUS(
    NO              NUMBER PRIMARY KEY
    , WRITER_NO     NUMBER
    , TITLE         VARCHAR2(1000)  NOT NULL
    , CONTENT       VARCHAR2(4000)  NOT NULL
    , HIT           NUMBER
    , ENROLL_DATE   DATE    DEFAULT SYSDATE
    , MODIFY_DATE   DATE
    , DEL_YN        CHAR(1) DEFAULT 'N'
);

-- 자유게시판
CREATE TABLE FREE_BOARD(
    NO              NUMBER PRIMARY KEY
    , WRITER_NO     NUMBER
    , TITLE         VARCHAR2(1000)  NOT NULL
    , CONTENT       VARCHAR2(4000)  NOT NULL
    , HIT           NUMBER
    , ENROLL_DATE   DATE    DEFAULT SYSDATE
    , MODIFY_DATE   DATE
    , DEL_YN        CHAR(1) DEFAULT 'N'
);

-- 게시판
CREATE TABLE BOARD(
    NO              NUMBER PRIMARY KEY
    , TENDINOUS_NO  NUMBER
    , FREE_BOARD_NO NUMBER
);

-- 댓글
CREATE TABLE REVIEW(
    NO              NUMBER PRIMARY KEY
    , BOARD_NO      NUMBER
    , CONTENT       VARCHAR2(4000)
    , WRITE_DATE    DATE    DEFAULT SYSDATE
    , MODIFY_DATE   DATE
    , DEL_YN        CHAR(1) DEFAULT 'N'
);

